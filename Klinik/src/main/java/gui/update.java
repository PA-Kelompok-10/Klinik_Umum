    private void pasientblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pasientblMouseClicked
        int index = pasientbl.getSelectedRow();
        if(index<0){
            JOptionPane.showMessageDialog(rootPane, "kntl");
        }else{
            String id = (String) pasientbl.getValueAt(index, 0);
            pasien pasienn = psienobj.returnValueById(id);
            idbox.setText(pasienn.getId());
            namadepanbox.setText(pasienn.getNamaDepan());
            namabelakangbox.setText(pasienn.getNamaBelakang());
            noteleponbox.setText(pasienn.getNoTelepon());
            nokerabatbox.setText(pasienn.getNoKerabat());
            tgllahirbox.setText(pasienn.getTanggalLahir());
            tglperiksabox.setText(pasienn.getTanggalPeriksa());
            jeniskelaminbox.setSelectedItem(pasienn.getJenisKelamin());
            alamatbox.setText(pasienn.getAlamat());
        }
//        
        
        
    }//GEN-LAST:event_pasientblMouseClicked
    

