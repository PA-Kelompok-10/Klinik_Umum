    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed

        int index = pasientbl.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(rootPane, "pilih data yang ingin diupdate ");
        }else{
            
            
            pasien update = new pasien(id, namaDepan, namaBelakang, noTelepon, noKerabat, tanggalLahir, tanggalPeriksa, jenisKelamin, alamat);
            
            
            
            
            
            
            
            update.id = (String)pasientbl.getValueAt(index, 0);
            update.namaDepan = namadepanbox.getText();
            update.namaBelakang = namabelakangbox.getText();
            update.noTelepon = noteleponbox.getText();
            update.noKerabat = nokerabatbox.getText();
            update.tanggalLahir = tgllahirbox.getText();
            update.tanggalPeriksa = tglperiksabox.getText();
            update.jenisKelamin = String.valueOf(jeniskelaminbox.getSelectedItem());
            update.alamat = alamatbox.getText();
            if(update.update()){
                JOptionPane.showMessageDialog(rootPane, "data diupdate");
            }
        }
//        
            
    }//GEN-LAST:event_updatebtnActionPerformed

